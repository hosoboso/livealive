for (let i = 0; i < trList.length; i++) {
	trList[i][0].forEach((row, index) => {
		row.addEventListener('click', () => {
			// すべての<tr>内のclass='selected-row'を削除
			trList[i][0].forEach(rows => {
				rows.classList.remove('selected-row');
			});
			
			//<div>内削除
			trList[i][1].replaceChildren();
			trList[i][2].replaceChildren();
			
			let enIndex;
			// 表のid「en_index」が付いたコンテンツを取得して数値化し-1して、敵パーティリストのインデックスにする
			//戦闘フィールド＆注意テキスト出力用関数で<div>内にテキストを出力
			const bluefont = `<br><span style="color:#0000FF;">青数字</span>は味方キャラ<br>矢印は向き`;
			
			if (i==0) {
				enIndex = Number( row.querySelector('#en_index').textContent )-1;
				trList[i][1].insertAdjacentHTML("afterbegin", battleTable(SaishueParty1[enIndex])[0]);
				trList[i][2].insertAdjacentHTML("afterbegin", battleTable(SaishueParty1[enIndex])[1] + bluefont);
			} else if (i==1) {
				enIndex = Number( row.querySelector('#en_index2').textContent )-1;
				trList[i][1].insertAdjacentHTML("afterbegin", battleTable(SaishueParty2[enIndex])[0]);
				trList[i][2].insertAdjacentHTML("afterbegin", battleTable(SaishueParty2[enIndex])[1] + bluefont);
			} else {
				enIndex = Number( row.querySelector('#en_index3').textContent )-1;
				trList[i][1].insertAdjacentHTML("afterbegin", battleTable(SaishueParty3[enIndex])[0]);
				trList[i][2].insertAdjacentHTML("afterbegin", battleTable(SaishueParty3[enIndex])[1] + bluefont);
			}

			//classにselected-rowを設定して、クリック行だけ太字にする
			row.classList.add('selected-row');
		});
	});
}


