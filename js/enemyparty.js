//取得経験値計算用の関数expCalc
function expCalc(aLV,eLV){
	let expText = "";
	if ( eLV === "-" ) {
		expText = "-";
	} else if ( ( eLV === `4以上` )&&(/^[1-9][0-9]?$/.test(aLV)) ) {
		expText = `56以上<br>(※下注意参照)`;	//ストレイボウ戦用
	} else if ( !(/^[1-9][0-9]?$/.test(aLV)) ) {
		expText = "-";	// 整数値判定
	} else {
		const
		// レベル値(string)を数値(number)変換
		aLvN = Number(aLV),
		// 敵パーティレベル[仮] - 味方キャラレベル
		lvSub = eLV - aLvN;
		//レベル差の値で分岐
		if ( 2 <= lvSub) {
			expText = Math.min((lvSub * 16 + 8),100);	//計算結果が100以上なら100に切り捨て処理
		} else if ( ( -2 <= lvSub ) && ( lvSub <= 1 ) ) {
			expText = Math.floor(36 / (3 - lvSub));
		} else {
			expText = 5;
		}
	}
	return expText;
}

